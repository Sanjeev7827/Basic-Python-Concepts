num1=int(input("Enter first number:"))
num2=int(input("Enter second number:"))

def add(num1,num2):
    return num1+num2

def sub(num1,num2):
    return num1-num2

def mul(num1,num2):
    return num1*num2

def div(num1,num2):
    return num1/num2

print("addition:",add(num1,num2))
print("subtraction:",sub(num1,num2))
print("multiplication:",mul(num1,num2))
print("division:",div(num1,num2))